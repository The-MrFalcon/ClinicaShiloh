                        <h1 class="mb-3 text-center rounded-pill">ESPERMOGRAMA: ASPECTOS MACROSCÓPICOS E MICROSCÓPICOS.</h1>
                        <p>O espermograma é o principal e mais comum exame para se averiguar a fertilidade masculina. Nele, são analisados diversos fatores, tanto macroscópicos quanto microscópicos.</p>
                        <p>Os fatores macroscópicos analisados são: cor, volume, viscosidade e pH da amostra. São de suma importância para averiguar possíveis problemas no aparelho reprodutor masculino.</p>
                        <p>Os fatores microscópicos analisados são: concentração dos espermatozóides, motilidade e morfologia. Além disso, também é visto o número de células redondas na amostra, com o intuito de averiguar o número de leucócitos, que podem sinalizar uma infecção, ou o número de células germinativas.</p>
                        <p>Além do espermograma, outros exames anexos também podem ser realizados para uma avaliação seminal mais detalhada. É possível avaliar a taxa de sobrevivência em 24h, a condição da amostra pós processamento seminal, o grau de fragmentação de DNA, a vitalidade dos espermatozóides, etc.</p>
