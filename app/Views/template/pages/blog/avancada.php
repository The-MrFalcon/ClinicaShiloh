                        <h1 class="mb-3 text-center rounded-pill">IDADE PATERNA AVANÇADA</h1>
                        <div class="post-teaser">
                            <p>Com a mudança da rotina, a inserção das mulheres no mercado de trabalho, o
                                número de casais que optam em ter filhos em idade mais avançada aumentou. Muito é
                                discutido acerca das intercorrências à fertilidade ocasionadas pela idade materna avançada,
                                entretanto é necessário considerar também a idade paterna como um fator de risco.
                                Pesquisas recentes questionam: os homens possuem capacidade reprodutiva
                                permanente? Tanaka et al, 2019 aborda em seu artigo que homens com mais de 40 anos
                                possuem um desbalanço na espermatogênese causando efeitos como alterações nos níveis
                                hormonais, danos ao DNA devido ao aumento de radicais livres, aumento de translocações,
                                deleções, fragmentação e mutações cromossômicas e a prevalência de células alteradas.
                                Além disso, foi observado também alterações epigenéticas nos espermatozoides
                                desses homens, o que aumenta a propagação de genes responsáveis pelo autismo,
                                esquizofrenia e bipolaridade, bem como o aumento de doenças autossômicas e outras
                                desordens neurológicas. Podemos citar também alterações nos parâmetros seminais, que
                                são concomitantes com o envelhecimento.</p>
                            <p>Ainda que a idade paterna avançada pareça ter uma menor influência nos resultados
                                nas Técnicas de Reprodução Assistida, quando comparados à idade materna, mais estudos
                                são necessários para elucidar essa associação., uma vez que a literatura atual é limitada e
                                sujeita ao viés devido a idade materna.</p>
                            <p>É importante, ainda, a importância de esclarecer e aconselhar os homens dispostos
                                a adiar a paternidade e quanto aos riscos existentes e quanto às opções disponíveis, como
                                a criopreservação do sêmen em uma idade mais jovem, bem como a prática de hábitos de
                                vida saudáveis. Em homens com mais idade, sobretudo acima de 50 anos, deve-se
                                considerar o uso de testes genéticos de pré-implantação para detectar anormalidades
                                embrionárias e selecionar embriões euplóides para a transferência embrionária.
                                Em síntese, a idade avançada é um parâmetro a ser considerado tanto entre as
                                mães quantos nos pais, sobretudo no tratamento de Reprodução Assistida, pois ambos
                                casos influenciam a formação de embriões e blastocistos.</p>
