                        <h1 class="mb-3 text-center rounded-pill">FRAGMENTAÇÃO DE DNA ESPERMÁTICO</h1>
                        <div class="post-teaser">
                            <p>Uma das causa possíveis para infertilidade masculina é a fragmentação do
                                DNA espermático, ou seja, quando material genético do espermatozoide não está
                                íntegro, o que prejudica a capacidade fértil do gameta e tornam as chances de
                                gestação natural muito pequenas.</p>
                                <p>Podemos citar dois tipos de fragmentação de DNA: a de fita simples e a de
                                fita dupla. A fragmentação de DNA de fita simples ocorre principalmente pela
                                presença de espécies reativas de oxigênio, fatores exógenos como dieta, exposição
                                à radiação, etilismo, tabagismo, uso de narcóticos, obesidade, sedentarismo e
                                outros fatores aos quais os homens são exposto no dia a dia. Também citamos
                                fatores endógenos, como a varicocele, doenças autoimunes, câncer e infecções no
                                trato reprodutivo. Quando há fragmentação de DNA de fita simples, os danos são
                                maiores, pois afetam a motilidade do espermatozoide, bem como o potencial de
                                fertilização, o que, consequentemente, reduz as taxas de gestação.</p>
                                <p>Alguns exames podem detectar a fragmentação de DNA de fita simples nos
                                pacientes como SDC, SCSA, tunel e ensaio cometa. Quando é detectada essa
                                alteração, durante a ICSI o embriologista opta pelo uso do gradiente descontínuo de
                                densidade, ou swim up com técnicas de processamento seminal.</p>
                                <p>Quando falamos sobre a fragmentação de DNA de fita dupla, associamos a
                                uma falha nos sistemas de reparo durante a meiose. Observamos durante o ciclo de
                                pacientes com essas alterações bloqueio no desenvolvimento embrionário, falha na
                                implantação de embriões e abortos espontâneos. Como nesse caso não há
                                alteração na motilidade dos espermatozoides, técnicas de processamento seminal
                                que visam a seleção através da motilidade não são eficientes. Então, é indicado o
                                uso de chips microfluídicos, como a Zymot, a qual seleciona espermatozoides sem
                                fragmentação de DNA. O ensaio cometa é o teste mais indicado para detectar essa
                                alteração.</p>
                                <p>O médico especialista em reprodução humana, sobretudo urologistas e
                                andrologistas são indicados para solicitar esse teste ao paciente. Esses
                                especialistas são capacitados a analisar a taxa de fragmentação do DNA
                                espermático de maneira individualizados, levando em consideração o histórico do
                                homem.</p>
