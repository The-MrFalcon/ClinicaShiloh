                        <h1 class="mb-3 text-center rounded-pill">OBESIDADE E FERTILIDADE</h1>
                        <div class="post-teaser">
                            <p>A obesidade é um acúmulo de gordura corporal anormal, ou excessiva que leva a
                                um risco para a saúde. Segundo a OMS, cerca de 39% dos adultos no mundo estão com
                                sobrepeso e 13% obesos. Além dos riscos cardiovasculares, diabetes e outros problemas
                                de saúde em pacientes com essa comorbidade, 12% possuem casos de infertilidade,
                                conforme já foi comprovado em diversos estudos.</p>
                            <p>Nos homens, podemos observar que a obesidade causa alterações no metabolismo
                                como queda da testosterona e alterações em outros perfis hormonais, como os níveis de
                                estrogênio. Também é observado alterações nos parâmetros seminais como redução da
                                concentração de espermatozoides e déficit no grau de motilidade espermática.
                                Outro ponto importante a ser levado em consideração, é que o aumento da
                                temperatura testicular criado pelo panículo adiposo do púbis e do cordão inguinal está
                                ligado à fragmentação de DNA. Além disso, há também o aumento do estresse oxidativo em
                                pacientes obesos, o que contribui para tal fato. A fragmentação de DNA aumenta a chance
                                de abortos, bem como que a gestação ocorra de forma natural.</p>
                            <p>Quando pensamos nessa comorbidade em relação às mulheres, podemos citar
                                distúrbios ovulatórios, que causam a inibição da ovulação e menstruação. Além disso, há
                                um estado inflamatório de baixo grau nos tecidos reprodutivos, ocasionado pelo excesso de
                                ácidos graxos livres. A obesidade também é responsável por alterações nos oócitos, bem
                                como nos embriões pré-implantação e aumento da leptina, o que afeta a esteroidogênese e
                                o desenvolvimento embrionário, ocasionando falhas nos tratamentos de reprodução
                                assistida e sucesso das festações.</p>
                            <p>Assim, fica evidente que os efeitos da obesidade são multifatoriais na função
                                reprodutiva masculina e feminina, afetando os gametas e embriões e como consequência
                                prejudica as chances de uma concepção natural, bem como aumenta os riscos de aborto.</p>
